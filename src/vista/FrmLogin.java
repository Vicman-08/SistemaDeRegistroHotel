    private void Login() {
    if (!txt_usuario.getText().isEmpty() && txt_password.getPassword().length > 0) {
        
        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
        Usuario usuario = new Usuario();
        usuario.setUsuario(txt_usuario.getText().trim());
        usuario.setPassword(new String(txt_password.getPassword()).trim());
        
        try {
            if (controlUsuario.loginUser(usuario)) {
                // Verificar el tipo de usuario
                if (usuario.getTipo() == 1) {
                    // Si es administrador (tipo 1), abrir la interfaz de administrador
                    FrmMenu menu = new FrmMenu();
                    menu.setVisible(true);
                    this.dispose();
                } else if (usuario.getTipo() == 2) {
                    // Si es recepcionista (tipo 2), abrir la interfaz de recepcionista
                    FrmRecepcionista recepcionista = new FrmRecepcionista(); // Aseg√∫rate de tener esta clase
                    recepcionista.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Tipo de usuario desconocido");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al intentar iniciar sesi√≥n: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Por favor ingrese el usuario y la contrase√±a");
    }
}


